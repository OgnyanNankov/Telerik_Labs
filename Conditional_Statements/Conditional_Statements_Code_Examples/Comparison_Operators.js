
// In JavaScript, comparison operators such as >, <, <=, and >= are crucial for controlling the flow of logic in programs. 
// These operators allow you to compare two values, determining whether one is greater than, less than, equal to, or not equal to another. 
// The results of these comparisons can be used to make decisions in your code.
// Comparison operators like >, <, <= and >= can be used to compare both numeric and string types. For example:

console.log(5 > 3);  // true
console.log(10 <= 15);  // true
console.log('apple' < 'banana'); // true - В този пример резултатът е true, тъй като в лексикографската подредба "apple" идва преди "banana".

