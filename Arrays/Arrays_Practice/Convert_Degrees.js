// Task 'Convert Degrees'                                                       // Преобразува градуси от Целзий (°C) в Фаренхайт (°F). Формулата, която използваме, е: °F = (°C × 9/5) + 32
let input = [
    '0 15 30'                                                                   // симулиран вход – един ред, съдържащ три стойности (температури в °C), разделени с интервал. '0 15 30' – трите стойности, които ще преобразуваме във °F.
];

let print = this.print || console.log;
let gets = this.gets || ((arr, index) => () => arr[index++])(input, 0);

let n = gets().split(' ');                                                     //  Четем реда '0 15 30' и го разделяме по интервал: Коментар 1

for (let i = 0; i < n.length; i++) {                                           //  Цикъл, който обхожда масива n елемент по елемент.
    let number = n[i] * 9 / 5 + 32;                                            //  Тук преобразуваме текущата стойност от °C към °F: Коментар 2
    console.log(Math.round(number));
}

