// Strings (Стрингове)

// Стрингът може да се разглежда като колекция от символи.
// Можеш да достъпиш символите му чрез индекси:
let word = 'JavaScript';
let firstLetter = word[0];
console.log(firstLetter); // извежда 'J'

// Но не можеш да променяш символите в стринг, както в масив:
word[0] = 'H'; // Стрингът ще остане със стойност 'JavaScript'

// *************************
// Преобразуване на масиви в стринг и обратно

// Преобразуване на стрингове в масиви: Стрингът е колекция от символи. Тъй като е и итерируем обект, лесно можем да го превърнем в масив.
// Преобразуване на масиви в стринг: Можем да обединим всички елементи на масива в стринг, като използваме метода join().

let letters1 = ['h', 'e', 'l', 'l', 'o'];

let word = letters1.join();             // 'h,e,l,l,o' — по подразбиране използва запетая като разделител
let hello = letters1.join('');          // 'hello' — без разделител (празен стринг)
let helloWithDashes = letters1.join('-'); // 'h-e-l-l-o'

// Използването на String() върху масив е равносилно на join() без аргумент.
// String() не е много полезна функция за масиви, защото винаги използва запетая.

let letters2 = ['h', 'e', 'l', 'l', 'o'];

let word = String(letters2);      // 'h,e,l,l,o'
let secondWord = letters2.join(); // 'h,e,l,l,o'

let same = word === secondWord;  // true

