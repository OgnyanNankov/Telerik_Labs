// ['J', 'a', 'v', 'a', 'S', 'c', 'r', 'i', 'p', 't']

// ***********************
// Разлика между Array.from() и String.split()

// Array.from() винаги връща всички символи от стринга, независимо какво съдържа.
// String.split() може да раздели стринга по зададен разделител, като и премахва самия разделител от резултата.

// Пример 1: Без специални символи
let word = 'hello';
let fromArray = Array.from(word);
let splitArray = word.split('');
// И двата метода ще дадат същия резултат:
console.log(fromArray); // ['h', 'e', 'l', 'l', 'o']
console.log(splitArray); // ['h', 'e', 'l', 'l', 'o']
// Тук няма разлика – и двата метода обхождат всеки символ.

