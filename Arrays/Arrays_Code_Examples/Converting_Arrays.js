// Използване на метода split() на стринг

// Методът split() разделя стринг на масив от подстрингове, използвайки зададен разделител.
let word = 'JavaScript';
let letters3 = word.split('');
// ['J', 'a', 'v', 'a', 'S', 'c', 'r', 'i', 'p', 't']
// split('') разделя по всеки символ
// можеш също да подадеш конкретен разделител (напр. split(',') или split(' '))

// ************************
// Използване на метода Array.from()

