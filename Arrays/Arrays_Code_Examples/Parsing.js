// Parsing                                                                 // Преобразува стринг от числа в масив, конвертира ги от стрингове в числа, и отпечатва всяко число увеличено с 1.

let line = '1,2,3,4,5,6';                                                  // Имаме стринг line, съдържащ числа, разделени със запетая. Типът е string, не array.
let numbers = line.split(',').map(Number);                                 // Това е много мощен едноредов израз. Разбиваме го: .split(',') → превръща стринга в масив по запетаи: ['1', '2', '3', '4', '5', '6'], .map(Number) → преобразува всеки елемент в число чрез функцията Number. инален резултат: numbers = [1, 2, 3, 4, 5, 6];

console.log(numbers);

for (let i = 0; i < numbers.length; i++) {                                 // Започваме класически for цикъл, който ще обиколи всички елементи от масива numbers. i = 0 → първият елемент, i < numbers.length → ще се изпълнява, докато i е в границите на масива
  console.log(`Increment with one: ${numbers[i] + 1}`);
}


// Пример: За numbers[0] = 1 → ще отпечата:
// Increment with one: 2
// Използваме template string ${} за вграждане на стойност в текста.

// Обобщение:
// Стъпка	                     Метод	         Резултат
// Стринг → масив от текстове	 split(',')	     ['1', '2', '3', ...]
// Текст → числа	             .map(Number)	 [1, 2, 3, ...]
// Обход и печат	             for + ${}	     Показва всяко число + 1

// Целият резултат:
// [1, 2, 3, 4, 5, 6]
// Increment with one: 2
// Increment with one: 3
