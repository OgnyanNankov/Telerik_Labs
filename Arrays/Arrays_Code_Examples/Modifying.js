// Modifying an array                                                        // Показва, че не можеш директно да променяш символи в стринг, и демонстрира как се извлича част от стринга чрез .substring().
let str = 'Telerik Academy';                                                 // Създаваме стринг със стойност 'Telerik Academy'.

str[0] = 'A';                                                                // This will not update the string. Виж коментар 1.

let newStr = str.substring(0, 2);                                            // Извлича подстринг от str, започвайки от индекс 0 до индекс 2 (но не включва индекс 2). Резултат: 'Te'. (ще получим символите на позиции 0 и 1: 'T' и 'e').

console.log(str);
console.log(newStr); // Sub-string from 0 to 2nd element (excluding)


// Коментар 1: Важно: В JavaScript не можеш да променяш отделни символи на стринг чрез индекси.
// str[0] = 'A' няма да даде грешка, но и няма да промени нищо. Стрингът остава 'Telerik Academy'.
// Това е така, защото стринговете са immutable – не могат да бъдат модифицирани символ по символ, а само през създаване на нови стрингове.

