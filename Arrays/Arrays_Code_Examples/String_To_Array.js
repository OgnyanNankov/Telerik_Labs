// Sting to Array                                         // Скриптът показва как методът .split() преобразува стринг в масив по различен начин в зависимост от разделителя.
let greeting1 = 'hello';                                  // Създаваме стринг с текст 'hello'.
let array1 = greeting1.split('');                         // Методът .split('') разделя стринга по всеки символ. Резултат: array1 = ['h', 'e', 'l', 'l', 'o']; .split('') казва: „раздели всяка буква като отделен елемент в масив“.

console.log(array1);                                      // Отпечатва масива array1: ['h', 'e', 'l', 'l', 'o']

let greeting2 = 'h,e,l,l,o';                              // Създаваме нов стринг, който вече има запетаи между буквите.
let array2 = greeting2.split(',');                        // Разделяме стринга по символа запетая ,. Резултат: array2 = ['h', 'e', 'l', 'l', 'o']; - Тук .split(',') казва: „раздели където има запетая“. Коментар 1:

console.log(array2);                                      // Отпечатва масива array2: ['h', 'e', 'l', 'l', 'o']


// Обобщение: .split(separator)
// Разделител	Поведение	               Пример	                    Резултат
// ''	        Разделя по всеки символ	   'hello'.split('')	        ['h', 'e', 'l', 'l', 'o']
// ','	        Разделя по запетаи	       'a,b,c'.split(',')	        ['a', 'b', 'c']
// ' '	        Разделя по интервали	   'hi there'.split(' ')	    ['hi', 'there']
// Сравнение:
// Променлива	Стойност на стринга	   Разделител	Резултат
// greeting1	'hello'	               ''	        ['h', 'e', 'l', 'l', 'o']
// greeting2	'h,e,l,l,o'	           ','	        ['h', 'e', 'l', 'l', 'o']
