// Array From String                                              // Показва как Array.from() преобразува стринг в масив – включително когато има знаци за разделяне, като запетая.
let greeting1 = 'hello';                                          // Създаваме стринг 'hello'.
let array1 = Array.from(greeting1);                               //Array.from() създава масив от всяка итерируема структура, напр. стринг. Тъй като 'hello' е стринг, Array.from() ще изтегли всеки отделен символ. Резултат: ['h', 'e', 'l', 'l', 'o'] Подобно на .split(''), но по-четим и по-гъвкав за някои случаи.

console.log(array1);                                              // Изход: ['h', 'e', 'l', 'l', 'o']

let greeting2 = 'h,e,l,l,o';                                      // Нов стринг – но тук вече има вградени запетаи.
let array2 = Array.from(greeting2);                               // Отново – Array.from() ще направи масив от всеки символ, независимо дали това е буква или запетая. Виж коментар 1.

console.log(array2);                                              // Изход: ['h', ',', 'e', ',', 'l', ',', 'l', ',', 'o']

// Коментар 1: Важно е да се отбележи, че Array.from() не разделя по запетаи, а просто обхожда всеки символ в стринга. Резултат: ['h', ',', 'e', ',', 'l', ',', 'l', ',', 'o'] – запетаите са включени в масива.

