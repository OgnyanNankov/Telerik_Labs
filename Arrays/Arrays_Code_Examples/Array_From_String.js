// .split(',')	       Разделя по запетая	                                       'a,b,c'.split(',')	  ['a', 'b', 'c']
// Array.from(str)	   Преобразува итерируем обект в масив от всички символи	   Array.from('abc')	  ['a', 'b', 'c']
// Array.from('a,b')   Обхожда буква-запетая-буква → няма разделяне		                                  ['a', ',', 'b']
